#include "core/strings/ft_strings.h"

int	ft_ffs(int i)
{
	int	pos;

	if (i == 0)
		return (0);
	pos = 1;
	while ((i & 1) == 0)
	{
		i >>= 1;
		pos++;
	}
	return (pos);
}
